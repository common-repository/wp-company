	 * Fired for each blog when the plugin is activated.
	 *
	 * @since    1.0.0
	 */
	private static function single_activate() {
		// @TODO: Define activation functionality here
	}

	/**
	 * Fired for each blog when the plugin is deactivated.
	 *
	 * @since    1.0.0
	 */
	private static function single_deactivate() {
		// @TODO: Define deactivation functionality here
	}

	/**
	 * Load the plugin text domain for translation.
	 *
	 * @since    1.0.0
	 */
	public function load_plugin_textdomain() {

		$domain = $this->plugin_slug;
		$locale = apply_filters( 'plugin_locale', get_locale(), $domain );

		load_textdomain( $domain, trailingslashit( WP_LANG_DIR ) . $domain . '/' . $domain . '-' . $locale . '.mo' );

	}

	/**
	 * Register and enqueue public-facing style sheet.
	 *
	 * @since    1.0.0
	 */
	public function enqueue_styles() {
		wp_enqueue_style( $this->plugin_slug . '-plugin-styles', plugins_url( 'assets/css/public.css', __FILE__ ), array(), self::VERSION );
	}

	/**
	 * Register and enqueues public-facing JavaScript files.
	 *
	 * @since    1.0.0
	 */
	public function enqueue_scripts() {
		wp_enqueue_script( $this->plugin_slug . '-plugin-script', plugins_url( 'assets/js/public.js', __FILE__ ), array( 'jquery' ), self::VERSION );
	}

	/**
	 *	Adds Shortcodes to grab the options by option name
	 *
	 *	@since 1.0.0
	 */
	public function wp_company_get_option($atts){
		// This is a list of approved options that can be used with the plugin
		$options_list = array(
			// Company Info
			'company_name',
			'company_address_1',
			'company_address_2',
			'company_country',
			'company_state',
			'company_city',
			'company_postal',
			// Company Social
			'company_facebook',
	 		'company_twitter',
	 		'company_googleplus',
	 		'company_linkedin',
	 		'company_pinterest',
	 		'company_tumblr',
	 		'company_vimeo',
	 		'company_youtube'
		);

		extract( 
			shortcode_atts( array(
	      		'option' => '',
	    	), $atts ) 
	    );


		if( in_array( $option, $options_list ) ) {
			$return_option = get_option($option);

			if( $option == 'company_facebook' )			{ $return_option = 'http://facebook.com/'.$return_option; }
			else if( $option == 'company_twitter' )		{ $return_option = 'http://twitter.com/'.$return_option; }
			else if( $option == 'company_googleplus' )	{ $return_option = 'https://plus.google.com/'.$return_option; }
			else if( $option == 'company_linkedin' )	{ $return_option = 'http://linkedin.com/company/'.$return_option; }
			else if( $option == 'company_pinterest' )	{ $return_option = 'http://pinterest.com/'.$return_option; }
			else if( $option == 'company_tumblr' )		{ $return_option = 'http://'.$return_option.'.tumblr.com'; }
			else if( $option == 'company_vimeo' )		{ $return_option = 'http://vimeo.com/'.$return_option; }
			else if( $option == 'company_youtube' )		{ $return_option = 'http://youtube.com/user/'.$return_option; }

			return $return_option;
		}
		else return '';
	}
}
